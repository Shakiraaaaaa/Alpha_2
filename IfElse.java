public class IfElse {
    public static void main(String args[])
    {
        int number=98;
        if(number>90)
        {
            System.out.println("A+");
        }
        else if(number>85)
        {
            System.out.println("A");
        }
        else
        {
            System.out.println("B+");
        }
    }
}
